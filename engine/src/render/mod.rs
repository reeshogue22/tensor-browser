fn get_char_bitmap_16(ch: char) -> [u8; 16] {
    let idx = ch as u32;
    if idx >= 32 && idx <= 126 {
        FONT_8X16[(idx - 32) as usize]
    } else {
        // Unknown glyph — filled box
        [0x00,0x00,0xFE,0x82,0x82,0x82,0x82,0x82,0x82,0x82,0x82,0xFE,0x00,0x00,0x00,0x00]
    }
}

/// Build font atlas for GPU compute — 16 bytes per char, chars 32-126 + unknown
pub fn build_font_atlas() -> Vec<u8> {
    let mut atlas = Vec::with_capacity(96 * 16);
    for ch in 32u8..=126 {
        let bitmap = get_char_bitmap_16(ch as char);
        atlas.extend_from_slice(&bitmap);
    }
    // Unknown glyph box
    atlas.extend_from_slice(&[0x00,0x00,0xFE,0x82,0x82,0x82,0x82,0x82,0x82,0x82,0x82,0xFE,0x00,0x00,0x00,0x00]);
    atlas
}

#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn test_canvas_basic() {
        let mut canvas = Canvas::new(100, 100);
        canvas.fill_rect(10.0, 10.0, 20.0, 20.0, Color { r: 255, g: 0, b: 0, a: 255 });
        // Check a red pixel
        let i = ((15 * 100 + 15) * 4) as usize;
        assert_eq!(canvas.pixels[i], 255); // R
        assert_eq!(canvas.pixels[i + 1], 0); // G
        assert_eq!(canvas.pixels[i + 2], 0); // B
    }

    #[test]
    fn test_bmp_header() {
        let canvas = Canvas::new(2, 2);
        let bmp = canvas.to_bmp();
        assert_eq!(&bmp[0..2], b"BM");
        assert!(bmp.len() > 54); // header + pixels
    }
}

